    private void btnGrupoAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrupoAgregarActionPerformed
        PreparedStatement ps = null;
        String grupo = txtGrupoAgregar.getText().toUpperCase();
        if(grupo.isEmpty()){
            return;
        }
        try {
            Conexion c = new Conexion();
            java.sql.Connection conn = c.getConexion();
            ps = conn.prepareStatement("INSERT INTO tb_grupos (grupo) VALUES (?)");
            ps.setString(1, grupo);

            ps.execute();

            JOptionPane.showMessageDialog(null, "Grupo agregado");
            txtGrupoAgregar.setText(null);
            
            limpiarDatos();
            
            c.close(conn);
            
            cargarDatos();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No sepudo agregar el grupo, posiblemente ya exista un grupo con este nombre");
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnGrupoAgregarActionPerformed

