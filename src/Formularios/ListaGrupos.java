    private void tbGrupoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbGrupoMouseClicked
        if(this.idRol == 2){
            return;
        }
        txtGrupoModificar.setEnabled(true);
        btnGrupoModificar.setEnabled(true);
        btnEliminarGrupo.setEnabled(true);
        PreparedStatement ps = null;
        ResultSet rs = null;
        try {
            Conexion c = new Conexion();
            java.sql.Connection conn = c.getConexion();

            int Fila = tbGrupo.getSelectedRow();
            String codigo = tbGrupo.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT id, grupo FROM tb_grupos WHERE grupo=?");
            ps.setString(1, codigo);
            rs = ps.executeQuery();

            while (rs.next()) {
                txtGrupoModificar.setText(rs.getString("grupo"));
                txtIdGrupo.setText(rs.getString("id"));
                txtGrupoAnterior.setText(rs.getString("grupo"));
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_tbGrupoMouseClicked

